def calculate_position_with_smooth_resistance(acceleration_data, dt, resistance_factor):
    position_x = np.zeros(len(acceleration_data))
    position_y = np.zeros(len(acceleration_data))
    velocity_x = np.zeros(len(acceleration_data))
    velocity_y = np.zeros(len(acceleration_data))

    for i in range(1, len(acceleration_data)):
        # Aplicando resistência linear suavizada à aceleração
        acceleration_x = acceleration_data[i, 0] - resistance_factor * velocity_x[i - 1]
        acceleration_y = acceleration_data[i, 1] - resistance_factor * velocity_y[i - 1]

        # Atualizando velocidade e posição
        velocity_x[i] = velocity_x[i - 1] + acceleration_x * dt
        velocity_y[i] = velocity_y[i - 1] + acceleration_y * dt
        position_x[i] = position_x[i - 1] + velocity_x[i] * dt
        position_y[i] = position_y[i - 1] + velocity_y[i] * dt

    return position_x, position_y


